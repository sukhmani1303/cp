void solve(){

    int m,x,y;
    cin>>m>>x>>y;
    int cops[m];

    int max_house = x*y;

    for(int i = 0; i<m; i++ ){
        cin>>cops[i];
    }

    int houses[101] = {0};

    for(int i = 0; i<m; i++){

        int left_house = 1, right_house = 100; // occupied houses

        // leftmost
        if(cops[i] - max_house>0){
            left_house = cops[i] - max_house;
        }
        if(cops[i] + max_house<100){
            right_house = cops[i] + max_house;
        }

        // mark houses

        for(int i = left_house; i<=right_house; i++){
            houses[i] = 1;
        }
    }

    int cnt = 0;

    for( int i =1; i<=101; i++){
        if (houses[i] ==1 ){
            cnt++;
        }
    } 

    cout<<100-cnt<<ln;   

}
int main()
{
    fast_cin();
    ll t;
    cin >> t;
    for(int it=1;it<=t;it++) {
        solve();
    }
    return 0;
}