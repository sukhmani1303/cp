void solve(){
    
    ll n;
    cin >> n;
    
    ll s = 1, e = n, ans = 0;
    ll mid = s + (e-s)/2;
    while(s<=e){
        if (mid*mid + mid - 2*n <= 0){
            s = mid+1;
            ans = mid;
        }

        else if(mid*mid + mid - 2*n > 0) e = mid-1;
        mid = s + (e-s)/2;
    }
    cout<<ans<<ln;
}
